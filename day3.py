def part_one():
    levels = input.split('\n')
    tree_count = 0
    index = 3
    for idx, level in enumerate(levels, 1):
        # skip first level
        if idx == 1:
            continue

        if level == "":
            continue

        val = level[index]
        print(val)
        if val == "#" or val == "X":
            tree_count += 1
        if (index + 3) <= 30:
            index += 3
        else:
            if index == 28:
                index = 0
            elif index == 29:
                index = 1
            elif index == 30:
                index = 2
    print(tree_count)

